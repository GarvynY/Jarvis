_UNVERIFIABLE_NOTE = "（注：本节来源代理不明确，内容需人工核实）"


def _build_sources_summary(outputs: list[AgentOutput]) -> str:
    """Build sources_summary from SourceRef objects only — never fabricated."""
    seen_urls: set[str] = set()
    lines: list[str] = []

    for o in outputs:
        for src in o.sources:
            if src.url in seen_urls:
                continue
            seen_urls.add(src.url)
            pub = f"  发布: {src.published_at}" if src.published_at else ""
            lines.append(f"- {src.title or src.url} [{src.source}]{pub}")

    return "\n".join(lines) if lines else ""


def _sanitize_text(text: str, banned_terms: list[str]) -> str:
    """Replace every banned term with _SAFE_REPLACEMENT."""
    for term in banned_terms:
        if term in text:
            text = text.replace(term, _SAFE_REPLACEMENT)
    return text


def _apply_banned_filter(
    brief: ResearchBrief,
    banned_terms: list[str],
) -> ResearchBrief:
    """
    Post-generation safety pass.

    Scans conclusion, user_notes, and every section's content.
    Replaces any banned term in-place.  Returns the (mutated) brief.
    """
    if not banned_terms:
        return brief

    brief.conclusion = _sanitize_text(brief.conclusion, banned_terms)
    brief.user_notes = _sanitize_text(brief.user_notes, banned_terms)

    for sec in brief.sections:
        sec.content = _sanitize_text(sec.content, banned_terms)

    return brief


# ── Public class ──────────────────────────────────────────────────────────────

class SupervisorReportWriter:
    """
    Section-based supervisor — generates ResearchBrief from coordinator outputs.

    Protocol:
        brief = await SupervisorReportWriter().run(task, preset, outputs, cost)

    Thread model:
        _call_llm() runs in a fresh short-lived ThreadPoolExecutor per-run so
        asyncio.run() exits cleanly without waiting for a class-level pool.
    """

    async def run(
        self,
        task: ResearchTask,
        preset: ResearchPreset,
        outputs: list[AgentOutput],
        cost_estimate: CostEstimate,
    ) -> ResearchBrief:
        """
        Generate ResearchBrief.

        Tries the LLM path first; falls back to deterministic brief on failure.
        Applies banned-term filter regardless of which path was taken.
        """
        brief: ResearchBrief | None = None

        # ── ContextPack path (non-fatal) ─────────────────────────────────────
        context_pack: ContextPack | None = None
        try:
            with EvidenceStore() as store:
                context_pack = store.build_context_pack(
                    task, preset, outputs,
                    max_chunks_per_section=_MAX_CHUNKS_PER_SECTION,
                    token_budget=_CONTEXT_PACK_TOKEN_BUDGET,
                    max_chunk_tokens=_MAX_CHUNK_TOKENS,
                    section_token_reserves=_SECTION_TOKEN_RESERVES,
                    safe_user_context=task.safe_user_context,
                )
            if not context_pack.items:
                context_pack = None
        except Exception:  # noqa: BLE001
            _log.warning("ContextPack build failed; falling back to agent prompts",
                         exc_info=True)
            context_pack = None

        # ── LLM path ─────────────────────────────────────────────────────────
        try:
            brief = await _run_llm_with_json_retries(
                task, preset, outputs, cost_estimate,
                context_pack=context_pack,
            )
        except Exception:  # noqa: BLE001
            brief = None

        # ── Fallback path ─────────────────────────────────────────────────────
        if brief is None:
            brief = _build_fallback_brief(task, preset, outputs, cost_estimate)

        # ── Post-generation safety filter ─────────────────────────────────────
        brief = _apply_banned_filter(brief, preset.banned_terms)

        return brief
