"""
Phase 9 Step 5 — Section-based supervisor.

Reads AgentOutputs produced by the coordinator and generates a ResearchBrief
whose sections are driven entirely by ResearchPreset.report_sections.

Rules (hard):
  - At most THREE LLM calls per run: one initial call plus two JSON-repair
    retries when parsing/validation fails.
  - LLM prompt uses only AgentOutput.{findings, summary, risks, missing_data,
    sources} — never prior/external knowledge.
  - Disclaimer is hardcoded; never generated by the LLM.
  - sources_summary is built from SourceRef objects; never fabricated.
  - Post-generation safety filter strips preset.banned_terms from all text.
  - If LLM fails/times-out → deterministic fallback from agent summaries.
  - No network access, no Tavily, no memory, no raw logs.

Thread model:
  _call_llm() is blocking (Anthropic HTTP).  SupervisorReportWriter owns a
  short-lived per-run ThreadPoolExecutor so asyncio.run() does not hang.
"""

